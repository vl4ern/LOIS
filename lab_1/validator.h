#ifndef VALIDATOR_H
#define VALIDATOR_H

#include "formula.h"

/****************************************************************************************
 * Модуль validator
 *
 * НАЗНАЧЕНИЕ:
 * Проверяет корректность введённой формулы сокращённого языка логики высказываний.
 *
 * Формула считается корректной, если она соответствует одному из видов:
 * - A
 * - 0
 * - 1
 * - (-A)
 * - (A/\B)
 * - (A\/B)
 * - (A->B)
 * - (A~B)
 *
 * Вместо A и B могут быть любые корректные подформулы.
 ****************************************************************************************/

// Проверяет формулу на корректность.
// Если формула некорректна, записывает причину ошибки в errorMessage.
bool validateFormula(const char formula[], char errorMessage[]);

#endif